#!/usr/bin/env node
/**
 * Generates lib/coastlines.ts from world-atlas/land-110m.json.
 * Run: node scripts/gen-coastlines.mjs
 *
 * Strategy:
 *  1. Decode topojson arcs (delta-encoded quantized coords)
 *  2. Convert to WGS84 lat/lng
 *  3. Skip every SKIP points to reduce density
 *  4. Round to 1 decimal place (11km precision — enough for a 300px globe)
 *  5. Drop segments shorter than MIN_PTS points after simplification
 *  6. Write as a compact TS constant
 */

import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))

const SKIP = 8 // keep 1 in every SKIP points
const MIN_PTS = 3 // minimum points per segment to keep

const topo = JSON.parse(
  readFileSync(resolve(__dirname, '../node_modules/world-atlas/land-110m.json'), 'utf8')
)

const { scale, translate } = topo.transform
const arcs = topo.arcs

// Decode a single topojson arc to WGS84 [lat, lng] pairs
function decodeArc(arcIdx) {
  const raw = arcIdx < 0 ? [...arcs[~arcIdx]].reverse() : arcs[arcIdx]
  let x = 0,
    y = 0
  const pts = []
  for (const [dx, dy] of raw) {
    x += dx
    y += dy
    const lng = x * scale[0] + translate[0]
    const lat = y * scale[1] + translate[1]
    pts.push([lat, lng])
  }
  return pts
}

// Collect all rings from all polygons in the land MultiPolygon
function ringsfromGeom(geom) {
  const rings = []
  if (geom.type === 'Polygon') {
    for (const arcRefs of geom.arcs) {
      const pts = []
      for (const ref of arcRefs) pts.push(...decodeArc(ref))
      rings.push(pts)
    }
  } else if (geom.type === 'MultiPolygon') {
    for (const poly of geom.arcs) {
      for (const arcRefs of poly) {
        const pts = []
        for (const ref of arcRefs) pts.push(...decodeArc(ref))
        rings.push(pts)
      }
    }
  }
  return rings
}

const landObj = topo.objects.land
const allRings = []
if (landObj.type === 'GeometryCollection') {
  for (const geom of landObj.geometries) allRings.push(...ringsfromGeom(geom))
} else {
  allRings.push(...ringsfromGeom(landObj))
}

// Simplify: take every SKIP-th point, round to 1dp
const segments = []
for (const ring of allRings) {
  const simplified = []
  for (let i = 0; i < ring.length; i += SKIP) {
    const [lat, lng] = ring[i]
    simplified.push([Math.round(lat * 10) / 10, Math.round(lng * 10) / 10])
  }
  // close the ring if not already closed
  if (simplified.length > 0 && simplified[0][0] !== simplified[simplified.length - 1][0]) {
    simplified.push(simplified[0])
  }
  if (simplified.length >= MIN_PTS) segments.push(simplified)
}

const json = JSON.stringify(segments)
const output = `// Auto-generated by scripts/gen-coastlines.mjs — do not edit
// Source: world-atlas/land-110m.json (Natural Earth 110m resolution)
// ${segments.length} segments, ${segments.reduce((n, s) => n + s.length, 0)} total points
export const COASTLINES: [number, number][][] = ${json}\n`

const outPath = resolve(__dirname, '../lib/coastlines.ts')
writeFileSync(outPath, output)
console.log(
  `Written ${(output.length / 1024).toFixed(1)} KB to lib/coastlines.ts (${segments.length} segments)`
)
